
// the post increment and decrement
let x = 9;
let y = x++; //first let y = x then let x = x + 1
console.log(y); // 9
console.log(x); // 10
/* 
how it works:
the y is set to value of x then x gets incremented by 1
*/
// the pre increment and decrement
let a = 10;
let b = ++a; // first let a = a + 1  then let b = a
console.log(a); // 11
console.log(b); // 11
/*
how it works:
the a variable is incremented by 1 then the b variable is the value of a after increment
*/
